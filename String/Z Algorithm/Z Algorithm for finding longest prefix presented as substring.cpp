using namespace std;

string Zfunction(string s){
    int n=s.length();
    int z[n];
    int L = 0, R = 0;
    for (int i = 1; i < n; i++) {
      if (i > R) {
        L = R = i;
        while (R < n && s[R-L] == s[R]) R++;
        z[i] = R-L; R--;
      } else {
        int k = i-L;
        if (z[k] < R-i+1) z[i] = z[k];
        else {
          L = i;
          while (R < n && s[R-L] == s[R]) R++;
          z[i] = R-L; R--;
        }
      }
    }
    int ans;
    int mx=-1;
    for(int i=1;i<n;i++){
        if(z[i]>mx){
            ans=i;
            mx=z[i];
        }
    }
    string str(s.begin(),s.begin()+z[ans]);
    return str;
}
